import { pipelineService } from './pipeline-service.js';

export class ReportGenerator {
  constructor() {
    this.scheduledReports = new Map();
  }

  async generateHTMLReport(dataset, options = {}) {
    const { title = 'Data Analytics Report', includeStats = true, includeInsights = true } = options;
    
    const stats = dataset.columnStats || {};
    const rows = dataset.rows || [];
    
    let html = `<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <title>${title}</title>
  <style>
    body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif; max-width: 1000px; margin: 0 auto; padding: 20px; color: #1a1a2e; }
    h1 { color: #1a1a2e; border-bottom: 2px solid #4f46e5; padding-bottom: 10px; }
    h2 { color: #374151; margin-top: 30px; }
    .stat-grid { display: grid; grid-template-columns: repeat(auto-fit, minmax(180px, 1fr)); gap: 15px; margin: 15px 0; }
    .stat-card { background: #f9fafb; padding: 15px; border-radius: 8px; }
    .stat-label { color: #6b7280; font-size: 12px; }
    .stat-value { color: #1a1a2e; font-size: 24px; font-weight: bold; }
    table { width: 100%; border-collapse: collapse; margin-top: 10px; }
    th, td { padding: 10px; text-align: left; border-bottom: 1px solid #e5e7eb; }
    th { background: #f3f4f6; font-weight: 600; }
    .insight { padding: 12px; margin: 8px 0; background: #eff6ff; border-left: 3px solid #4f46e5; border-radius: 4px; }
    .chart-preview { margin: 15px 0; padding: 15px; background: #f9fafb; border-radius: 8px; }
    .footer { margin-top: 30px; padding-top: 20px; border-top: 1px solid #e5e7eb; color: #6b7280; font-size: 12px; }
  </style>
</head>
<body>
  <h1>📊 ${title}</h1>
  <p>Generated: ${new Date().toLocaleString()}</p>
  
  <h2>📈 Overview</h2>
  <div class="stat-grid">
    <div class="stat-card">
      <div class="stat-label">Total Rows</div>
      <div class="stat-value">${rows.length.toLocaleString()}</div>
    </div>
    <div class="stat-card">
      <div class="stat-label">Columns</div>
      <div class="stat-value">${dataset.columns?.length || 0}</div>
    </div>
    <div class="stat-card">
      <div class="stat-label">Data Type</div>
      <div class="stat-value">${dataset.schema?.dataTypeLabel || 'Generic'}</div>
    </div>
  </div>`;

    if (includeStats && Object.keys(stats).length > 0) {
      html += `\n  <h2>📉 Statistics</h2>\n  <table>\n    <tr><th>Column</th><th>Type</th><th>Count</th><th>Min</th><th>Max</th><th>Mean</th></tr>`;
      
      Object.entries(stats).forEach(([col, stat]) => {
        if (stat.type === 'numeric') {
          html += `\n    <tr><td>${col}</td><td>${stat.type}</td><td>${stat.count}</td><td>${stat.min?.toFixed(2) || '-'}</td><td>${stat.max?.toFixed(2) || '-'}</td><td>${stat.mean?.toFixed(2) || '-'}</td></tr>`;
        } else {
          html += `\n    <tr><td>${col}</td><td>${stat.type}</td><td>${stat.count}</td><td colspan="3">${stat.unique} unique values</td></tr>`;
        }
      });
      html += '\n  </table>';
    }

    if (includeInsights && dataset.insights?.length > 0) {
      html += '\n  <h2>💡 Key Insights</h2>';
      dataset.insights.slice(0, 10).forEach(insight => {
        const message = insight.message || JSON.stringify(insight);
        html += `\n  <div class="insight">${message}</div>`;
      });
    }

    if (dataset.charts?.length > 0) {
      html += '\n  <h2>📊 Visualizations</h2>';
      dataset.charts.slice(0, 7).forEach(chart => {
        html += `\n  <div class="chart-preview"><strong>${chart.title}</strong> (${chart.type})</div>`;
      });
    }

    html += `
  <div class="footer">
    Generated by InsightFlow Analytics | ${new Date().toISOString()}
  </div>
</body>
</html>`;

    return Buffer.from(html, 'utf-8');
  }

  async generateCSVExport(dataset) {
    const rows = dataset.rows || [];
    if (rows.length === 0) return Buffer.from('', 'utf-8');

    const headers = Object.keys(rows[0]);
    let csv = headers.join(',') + '\n';

    rows.forEach((row, idx) => {
      const values = headers.map(h => {
        const val = row[h];
        if (val == null) return '';
        const str = String(val);
        return str.includes(',') || str.includes('"') ? `"${str.replace(/"/g, '""')}"` : str;
      });
      csv += values.join(',') + '\n';
    });

    return Buffer.from(csv, 'utf-8');
  }

  async generateReport(dataset, options = {}) {
    const { format = 'html' } = options;
    
    switch (format) {
      case 'csv':
        return this.generateCSVExport(dataset);
      case 'html':
      default:
        return this.generateHTMLReport(dataset, options);
    }
  }

  async scheduleReport(config) {
    const { id, cronExpression, datasetId, format, recipients } = config;
    
    this.scheduledReports.set(id, {
      ...config,
      enabled: true,
      lastRun: null,
      nextRun: this.calculateNextRun(cronExpression)
    });

    return { success: true, id, nextRun: this.calculateNextRun(cronExpression) };
  }

  calculateNextRun(cronExpression) {
    return new Date(Date.now() + 60000);
  }

  async runScheduledReports() {
    const results = [];
    const now = new Date();

    for (const [id, report] of this.scheduledReports) {
      if (!report.enabled) continue;
      if (report.nextRun <= now) {
        try {
          const dataset = await pipelineService.getDataset(report.datasetId);
          if (dataset) {
            const buffer = await this.generateReport(dataset, { format: report.format });
            results.push({ id, status: 'generated', size: buffer.length });
          }
        } catch (error) {
          results.push({ id, status: 'error', error: error.message });
        }
        
        report.lastRun = now;
        report.nextRun = this.calculateNextRun(report.cronExpression);
      }
    }

    return results;
  }
}

export const reportGenerator = new ReportGenerator();